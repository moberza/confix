class CodePiece:
    def __init__(self, start_lineno, lines):
        self.start_lineno_ = start_lineno
        self.lines_ = lines
        pass
    def start_lineno(self):
        return self.start_lineno_
    def lines(self):
        return self.lines_
    pass
